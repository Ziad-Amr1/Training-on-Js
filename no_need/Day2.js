let degree = 85;

if (degree % 2 === 0) {
    console.log('Even Degree');
} else {
    console.log('Odd Degree');
}

if (degree === 100) {
    console.log('You are a genius!');
} else if (degree >= 90) {
    console.log('Excellent 🎉');
} else if (degree >= 70) {
    console.log('Good 😊');
} else if (degree >= 50) {
    console.log('Pass 🙂');
} else {
    console.log('Fail ❌');
}